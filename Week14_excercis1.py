value = int(input("Enter a numerical value: ")) #request user input of a number


#Print letter grade based on numerical value provided

if  value >= 90: print ('A')
if  90 > value >= 80: print('B')
if  80 > value >= 70: print("C")
if  70 > value >= 60: print("D")
if  60 > value: print("F")


